    private void Agregar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Agregar
        creado = false;
        if(jTextField1.getText().trim().length() > 0){
            nombre = jTextField1.getText();
            codigoBarras = jTextField2.getText();
            try{
               cantidad = Integer.parseInt(jTextField3.getText());
               res = new Producto(nombre, codigoBarras, cantidad);
               creado = true;
               this.dispose();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Datos Incorrectos", "Abusado !!!", JOptionPane.ERROR_MESSAGE, ic);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Ponle un nombre en el campo indicado", "Abusado !!!", JOptionPane.ERROR_MESSAGE, ic);
        }
    }//GEN-LAST:event_Agregar

